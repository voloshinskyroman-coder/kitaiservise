// Эвристика тональности ответа на холодную рассылку KitaiService (доставка из Китая, не ремонт).
const POSITIVE_KW = [
  'интересно', 'интересует', 'актуально', 'актуален', 'актуальна', 'актуал',
  'хочу', 'хотим', 'сколько стоит', 'сколько', 'цена', 'прайс', 'тариф',
  'подробнее', 'расскажите', 'скажите', 'можно', 'хотелось бы',
  'планируем', 'планирую', 'думаем', 'договорились', 'запишите', 'звоните', 'пишите',
  'доставка', 'посылка', 'груз', 'товар', 'заказ', 'поставщик', 'выкуп', 'китай', 'карго',
]
const NEGATIVE_KW = [
  'не интересно', 'не актуально', 'не актуален', 'неактуально', 'неактуален', 'неактуальна',
  'не надо', 'не нужно', 'не требуется',
  'уже есть', 'свой карго', 'свой поставщик', 'уже вожу', 'свой логист', 'своя логистика',
  'уже договор', 'заключен договор', 'уже заключ',
  'не беспокойте', 'отпишите', 'отписался', 'спам',
  'нет спасибо', 'не хочу', 'не буду', 'не планируем', 'не планирую',
]

export function classifyReply(text: string | null): 'green' | 'red' | 'gray' {
  if (!text) return 'gray'
  const t = text.toLowerCase()
  if (NEGATIVE_KW.some((kw) => t.includes(kw))) return 'red'
  if (POSITIVE_KW.some((kw) => t.includes(kw))) return 'green'
  return 'gray'
}
